package Ex;

public class Member {
	 private String id;
	 private String name;
	 private int point;
	

	 // 변수 값 설정
void setID(String id) {
	this.id = id;
}

void setNAME(String name) {
	this.name = name;
}

void setPOINT(int point) {
	this.point = point;
}
	 
	// 변수 값 얻기
String getID() {
	return id;
}

String getNAME() {
	return name;
}

int getPOINT() {
	return point;
}
	
}


